package main.java.animals;

public abstract class Animal {
  String name;
  int age;
  String gender;
  int numOfLegs;
  boolean canFly;

  public String getName() {
    return name;
  }

  public int getAge() {
    return age;
  }

  public String getGender() {
    return gender;
  }

  public int getNumOfLegs() {
    return numOfLegs;
  }

  public boolean canFly() {
    return canFly;
  }
}